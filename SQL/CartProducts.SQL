-- SubQuery
SELECT DISTINCT CART_ID
FROM CART_PRODUCTS
WHERE CART_ID
IN
(SELECT DISTINCT CART_ID
FROM CART_PRODUCTS
WHERE NAME = "Milk") AND NAME = "Yogurt"

-- Other Solutions
-- SELF JOIN
SELECT DISTINCT C.CART_ID
FROM CART_PRODUCTS C, CART_PRODUCTS D
WHERE C.CART_ID == D.CART_ID
AND (C.NAME = "Milk" AND D.NAME = "Yogurt")

-- INTERSECT
SELECT CART_ID
FROM CART_PRODUCTS
WHERE NAME = "Milk"
INTERSECT
SELECT CART_ID
FROM CART_PRODUCTS
WHERE NAME = "Yogurt"